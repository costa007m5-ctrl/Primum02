ALTER TABLE movies ENABLE ROW LEVEL SECURITY;
ALTER TABLE streaming_providers ENABLE ROW LEVEL SECURITY;
ALTER TABLE admin_users ENABLE ROW LEVEL SECURITY;
ALTER TABLE profiles ENABLE ROW LEVEL SECURITY;
ALTER TABLE watch_history ENABLE ROW LEVEL SECURITY;
ALTER TABLE my_list ENABLE ROW LEVEL SECURITY;
ALTER TABLE app_settings ENABLE ROW LEVEL SECURITY;

-- Movies: Public Read
CREATE POLICY "Public Read Movies" ON movies FOR SELECT USING (true);
CREATE POLICY "Admin All Movies" ON movies FOR ALL USING (
    EXISTS (SELECT 1 FROM admin_users WHERE email = auth.email())
);

-- Providers: Public Read
CREATE POLICY "Public Read Providers" ON streaming_providers FOR SELECT USING (true);
CREATE POLICY "Admin All Providers" ON streaming_providers FOR ALL USING (
    EXISTS (SELECT 1 FROM admin_users WHERE email = auth.email())
);

-- Profiles: User can manage own
CREATE POLICY "User Manage Own Profiles" ON profiles FOR ALL USING (auth.uid() = user_id);

-- Settings: User can manage own
CREATE POLICY "User Manage Own Settings" ON app_settings FOR ALL USING (auth.uid() = user_id);

-- Watch History: User can manage via profiles
CREATE POLICY "User Manage Own History" ON watch_history FOR ALL USING (
    EXISTS (SELECT 1 FROM profiles WHERE profiles.id = watch_history.profile_id AND profiles.user_id = auth.uid())
);

-- My List: User can manage via profiles
CREATE POLICY "User Manage Own List" ON my_list FOR ALL USING (
    EXISTS (SELECT 1 FROM profiles WHERE profiles.id = my_list.profile_id AND profiles.user_id = auth.uid())
);

-- Admin Users: Allow authenticated users to see the list (to check if they are admins)
CREATE POLICY "Enable read for authenticated users" ON admin_users
FOR SELECT TO authenticated
USING (true);

