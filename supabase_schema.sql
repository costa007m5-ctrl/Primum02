-- 4. User Profiles (Multi-profile support)
CREATE TABLE IF NOT EXISTS profiles (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
    name TEXT NOT NULL,
    avatar_url TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 5. Watch History (Progress tracking)
CREATE TABLE IF NOT EXISTS watch_history (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    profile_id UUID REFERENCES profiles(id) ON DELETE CASCADE,
    movie_id BIGINT REFERENCES movies(id) ON DELETE CASCADE,
    last_position DECIMAL DEFAULT 0,
    updated_at TIMESTAMPTZ DEFAULT NOW(),
    UNIQUE(profile_id, movie_id)
);

-- 6. My List (Favorites/Bookmarks)
CREATE TABLE IF NOT EXISTS my_list (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    profile_id UUID REFERENCES profiles(id) ON DELETE CASCADE,
    movie_id BIGINT REFERENCES movies(id) ON DELETE CASCADE,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    UNIQUE(profile_id, movie_id)
);

-- 7. App Settings
CREATE TABLE IF NOT EXISTS app_settings (
    user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE PRIMARY KEY,
    theme TEXT DEFAULT 'dark',
    language TEXT DEFAULT 'pt-BR',
    autoplay_next BOOLEAN DEFAULT TRUE,
    show_logos BOOLEAN DEFAULT TRUE,
    subscription_plan TEXT,
    subscription_status TEXT CHECK (subscription_status IN ('active', 'pending', 'expired')),
    subscription_expires_at TIMESTAMPTZ,
    google_drive_token TEXT,
    google_drive_accounts JSONB DEFAULT '[]'::jsonb,
    category_backdrops JSONB DEFAULT '{}'::jsonb,
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

