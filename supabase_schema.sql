-- AUTHENTICATION TRIGGERS
-- ==========================================

-- Function to handle new user creation
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER AS $$
BEGIN
  -- Create default profile for the user if it doesn't exist
  INSERT INTO public.profiles (user_id, name, avatar_url)
  VALUES (new.id, COALESCE(split_part(new.email, '@', 1), 'Usuário'), 'https://upload.wikimedia.org/wikipedia/commons/0/0b/Netflix-avatar.png')
  ON CONFLICT DO NOTHING;

  -- Create default app settings if they don't exist
  INSERT INTO public.app_settings (user_id)
  VALUES (new.id)
  ON CONFLICT DO NOTHING;

  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER SET search_path = public;

-- Trigger to call the function after a user signs up
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE PROCEDURE public.handle_new_user();

