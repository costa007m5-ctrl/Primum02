-- 1. Movies Table
CREATE TABLE IF NOT EXISTS movies (
    id BIGINT PRIMARY KEY, -- TMDb ID
    title TEXT,
    name TEXT,
    original_name TEXT,
    backdrop_path TEXT,
    poster_path TEXT,
    logo_path TEXT,
    overview TEXT,
    first_air_date TEXT,
    release_date TEXT,
    vote_average DECIMAL(3,1),
    video_url TEXT,
    video_url_2 TEXT,
    file_name TEXT,
    genres TEXT,
    type TEXT CHECK (type IN ('movie', 'series')),
    actors TEXT,
    rating DECIMAL(3,1),
    runtime INTEGER,
    release_year INTEGER,
    watch_providers TEXT,
    last_rescanned_at TIMESTAMPTZ,
    is_hidden BOOLEAN DEFAULT FALSE,
    collection_id BIGINT,
    collection_name TEXT,
    collection_poster_path TEXT,
    collection_logo_path TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 2. Streaming Providers
CREATE TABLE IF NOT EXISTS streaming_providers (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    name TEXT NOT NULL,
    logo_url TEXT,
    priority INTEGER DEFAULT 0,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 3. Admin Users (Whitelist)
CREATE TABLE IF NOT EXISTS admin_users (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    email TEXT UNIQUE NOT NULL,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

